#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	ch;

	ch = (char) c;
	i = 0;
	while (s[i] != '\0' && s[i] != ch)
		i++;
	if (ch == s[i])
		return ((char *) s + i);
	return (NULL);
}
